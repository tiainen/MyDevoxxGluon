package com.gluonhq.otn.model;

public class Link {
    private String href;
    private String rel;
    private String title;

    public Link() {}

    public Link(String href, String rel, String title) {
        this.href = href;
        this.rel = rel;
        this.title = title;
    }

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }

    public String getRel() {
        return rel;
    }

    public void setRel(String rel) {
        this.rel = rel;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    @Override
    public String toString() {
        return "Link{" +
                "href='" + href + '\'' +
                ", rel='" + rel + '\'' +
                ", title='" + title + '\'' +
                '}';
    }
}
